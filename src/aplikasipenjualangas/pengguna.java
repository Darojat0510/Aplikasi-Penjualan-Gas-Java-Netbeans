    private void cariBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariBTActionPerformed
        // TODO add your handling code here:
        String cari = JOptionPane.showInputDialog(null, "Cari berdasarkan ID Pengguna", "Input", JOptionPane.QUESTION_MESSAGE);
        setKoneksi();
        String sql="select * from pengguna where idpengguna='"+ cari +"'";
        try {
            ResultSet rs=st.executeQuery(sql);
            if (rs.next())
            {
                idTF.setText(rs.getString(1));
                namaTF.setText(rs.getString(2));
                userTF.setText(rs.getString(3));
                passTF.setText(rs.getString(4));
                aksesCB.setSelectedItem(rs.getString(5));
                //button
                hapusBT.setEnabled(true);
                editBT.setEnabled(true);
            }
            else {
                JOptionPane.showMessageDialog(null,"Data tidak ditemukan");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_cariBTActionPerformed

